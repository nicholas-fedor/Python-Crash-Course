favorite_language = ' python '
print(favorite_language)

# removes whitespace from the right side of a string
rstripped_message = favorite_language.rstrip()
print(rstripped_message)

# removes whitespace from the left side of a string
lstripped_message = favorite_language.lstrip()
print(lstripped_message)

# removes whitespace from the entire string
stripped_message = favorite_language.strip()
print(stripped_message)